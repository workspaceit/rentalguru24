public class PictureSize{
    private int width;
    private int height;

    public PictureSize() {
        this.width = 0;
        this.height = 0;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
